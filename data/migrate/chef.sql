CREATE TABLE `chef`
(
    `chef_id`        int          NOT NULL COMMENT '厨师id',
    `name`           varchar(30)  NOT NULL DEFAULT '' COMMENT '厨师',
    `gender`         int          NOT NULL DEFAULT 0 COMMENT '性别 1:男 2:女',
    `rarity`         int          NOT NULL DEFAULT 0 COMMENT '稀有度',
    `origins`        varchar(255) NOT NULL DEFAULT '[]' COMMENT '获得来源列表',
    `gallery_id`     varchar(20)  NOT NULL DEFAULT '' COMMENT '图鉴id',
    `stirfry`        int          NOT NULL DEFAULT 0 COMMENT '炒技法数值',
    `bake`           int          NOT NULL DEFAULT 0 COMMENT '烤技法数值',
    `boil`           int          NOT NULL DEFAULT 0 COMMENT '煮技法数值',
    `steam`          int          NOT NULL DEFAULT 0 COMMENT '蒸技法数值',
    `fry`            int          NOT NULL DEFAULT 0 COMMENT '炸技法数值',
    `cut`            int          NOT NULL DEFAULT 0 COMMENT '切技法数值',
    `meat`           int          NOT NULL DEFAULT 0 COMMENT '肉类采集数值',
    `flour`          int          NOT NULL DEFAULT 0 COMMENT '面类采集数值',
    `fish`           int          NOT NULL DEFAULT 0 COMMENT '鱼类采集数值',
    `vegetable`      int          NOT NULL DEFAULT 0 COMMENT '菜类采集数值',
    `skill_id`       int          NOT NULL DEFAULT 0 COMMENT '技能 id',
    `ultimate_goals` varchar(255)  NOT NULL DEFAULT '[]' COMMENT '修炼任务id数组',
    `ultimate_skill` int          NOT NULL DEFAULT 0 COMMENT '修炼技能',
    `sweet`          int          NOT NULL DEFAULT 0 COMMENT '甜调味数值',
    `sour`           int          NOT NULL DEFAULT 0 COMMENT '酸调味数值',
    `spicy`          int          NOT NULL DEFAULT 0 COMMENT '辣调味数值',
    `salty`          int          NOT NULL DEFAULT 0 COMMENT '咸调味数值',
    `bitter`         int          NOT NULL DEFAULT 0 COMMENT '苦调味数值',
    `tasty`          int          NOT NULL DEFAULT 0 COMMENT '鲜调味数值',
    `img`            varchar(255) COMMENT '厨师图片',
    PRIMARY KEY (`chef_id`),
    UNIQUE KEY `uniq_gallery_id` (`gallery_id`)
) ENGINE = InnoDB
  DEFAULT CHARSET = utf8mb4;